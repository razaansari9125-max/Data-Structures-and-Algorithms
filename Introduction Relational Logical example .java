class RelationalLogicalExample {
    public static void main(String[] args) {
        int x = 10, y = 20;

        System.out.println("x == y : " + (x == y));
        System.out.println("x != y : " + (x != y));
        System.out.println("x > y  : " + (x > y));
        System.out.println("x < y  : " + (x < y));

        boolean a = true, b = false;
        System.out.println("a && b : " + (a && b));
        System.out.println("a || b : " + (a || b));
        System.out.println("!a     : " + (!a));
    }
}